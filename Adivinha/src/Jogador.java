public class Jogador {
	
	private String nome;
	private String numero;
	
	public Jogador() {
		this.setNome(nome);
		this.setNumero(numero);
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	

}

